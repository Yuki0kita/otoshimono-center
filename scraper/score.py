def score_item(item: FoundItem) -> int:
    """物件の珍品度を返す。0未満にはしない。"""
    text = f"{item.name} {item.features} {item.contents}"
    score = 0
    for keyword, weight in _KEYWORD_WEIGHTS.items():
        if keyword in text:
            score += weight
    for keyword, weight in _BORING_WEIGHTS.items():
        if keyword in item.name:
            score += weight
    return max(score, 0)
